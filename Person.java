class Person
{
    String name,cin;
    int age;


    public Person(String name, int age,String cin) {
        this.name = name;
        this.age = age;
        this.cin = cin;
    }


    public java.lang.String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }


    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }

    public String getCin() {
        return cin;
    }
    public void setCin(String age) {
        this.cin = cin;
    }


}